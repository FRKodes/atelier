	<h2>Paco Calles // Fábrica de letras y Logotipografía</h2>

	<p>
		<br><br>
		Es licenciado en Diseño Gráfico, por la Universidad Nacional Autónoma de México; con estudios de Maestría en Artes Visuales, por la Academia de San Carlos; Maestría en Gestión del Diseño Gráfico, por la Universidad Intercontinental y Maestría en Procesos de Diseño por la Universidad Popular Autónoma del Estado de Puebla. Ha dictado conferencias, impartido cursos y publicado artículos en México, Argentina, Venezuela, Colombia, Uruguay, Guatemala, España y París.
	</p>
	<p>
		Realiza una intensa labor docente desde 1991, ha sido profesor de licenciatura y posgrado en varias universidades dentro y fuera de México. Asimismo es editor y articulista de la revista tiypo, única publicación especializada en tipografía en el país. Coautor de los libros Ensayos sobre diseño tipográfico en México y Ensayos sobre diseño, tipografía y lenguaje.
	</p>
	<p>
		Es director general de tipografilia —Congreso Nacional de Tipografía en México— y coordinador del capítulo México de Letras Latinas y Tipos Latinos—Bienal Latinoamericana de Tipografía—.
	</p>
	<p>
		Fue presidente de la Asociación Mexicana de Escuelas de Diseño Gráfico, Encuadre. Actualmente es director de la Maestría en Diseño Tipográfico y de la Maestría en Diseño Editorial del Centro de Estudios Gestalt del puerto de Veracruz.
	</p>
	<p>
		Es además coordinador de los Diplomados en Tipografía en la EDINBA, UAM Xochimilco, UdeG y en la Universidad de Guanajuato. Es miembro de la Association Typographique Internationale (ATypI), miembro del consejo asesor de FOROALFA e integrante del colectivo UNOSTIPOSDUROS.	
	</p>

	<p>Taller de fábrica de letras</p>
	
	<p>OBJETIVO:</p>

	<p>
		El participante conocerá y analizará los factores que determinan la coherencia formal de las letras para el diseño y rediseño (fabricación) de logotipos, alfabetos y fuentes tipográficas.
	</p>

	<p>CONTENIDO:</p>
	
	<p>1. RENGLÓN TIPOGRÁFICO. 2. TRAZOS GENERADORES. 3. SIGNOS GENERADORES. 4. AJUSTES ÓPTICOS.</p>
	
